        public void SendBouyomi(string[] messages)
        {
            string sMessage = null;
            byte bCode = 0;
            Int16 iVoice = 1;
            Int16 iVolume = -1;
            Int16 iSpeed = -1;
            Int16 iTone = -1;
            Int16 iCommand = 0x0001;


            //引数処理
            switch (messages.Length) {
                case 0:
                    sMessage = "こんにちわ";
                    break;
                case 1:
                    sMessage = messages[0];
                    break;
                case 5:
                    iSpeed = Int16.Parse(messages[0]);
                    iTone = Int16.Parse(messages[1]);
                    iVolume = Int16.Parse(messages[2]);
                    iVoice = Int16.Parse(messages[3]);
                    sMessage = messages[4];
                    break;
                default:
                    Console.WriteLine("使用法1>BouyomiChanSample 文章");
                    Console.WriteLine("使用法2>BouyomiChanSample 速度(50-300) 音程(50-200) 音量(0-100) 声質(0-8) 文章");
                    return;
            }

            var bMessage = Encoding.UTF8.GetBytes(sMessage);
            var iLength = bMessage.Length;

            //棒読みちゃんのTCPサーバへ接続
            var sHost = this.IP; //棒読みちゃんが動いているホスト
            var iPort = this.Port;       //棒読みちゃんのTCPサーバのポート番号(デフォルト値 50001)
            TcpClient tc = null;
            try {
                tc = new TcpClient(sHost, iPort);
            }
            catch (Exception) {
                Console.WriteLine("接続失敗");
            }

            if (tc != null) {
                //メッセージ送信
                using (var ns = tc.GetStream()) {
                    using (var bw = new BinaryWriter(ns)) {
                        bw.Write(iCommand); //コマンド（ 0:メッセージ読み上げ）
                        bw.Write(iSpeed);   //速度    （-1:棒読みちゃん画面上の設定）
                        bw.Write(iTone);    //音程    （-1:棒読みちゃん画面上の設定）
                        bw.Write(iVolume);  //音量    （-1:棒読みちゃん画面上の設定）
                        bw.Write(iVoice);   //声質    （ 0:棒読みちゃん画面上の設定、1:女性1、2:女性2、3:男性1、4:男性2、5:中性、6:ロボット、7:機械1、8:機械2、10001～:SAPI5）
                        bw.Write(bCode);    //文字列のbyte配列の文字コード(0:UTF-8, 1:Unicode, 2:Shift-JIS)
                        bw.Write(iLength);  //文字列のbyte配列の長さ
                        bw.Write(bMessage); //文字列のbyte配列
                    }
                }

                //切断
                tc.Close();
            }
        }


        public async Task SendBouyomi(byte[] bytes)
        {
            try {
                await Task.Run(() =>
                {
                    var tc = new TcpClient(this.IP, this.Port);
                    using (var ns = tc.GetStream()) {
                        ns.Write(bytes, 0, bytes.Length);
                    }
                    tc.Close();
                }).ConfigureAwait(false);
            }
            catch (Exception e) {
                Logger.Error(e);
            }
        }
